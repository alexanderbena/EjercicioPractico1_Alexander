package com.biblioteca.biblioteca.service.impl;

/**
 *
 * @author alexa
 */
import com.biblioteca.biblioteca.domain.Category;
import com.biblioteca.biblioteca.repository.CategoryRepository;
import com.biblioteca.biblioteca.service.CategoryService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;

@Service
public class CategoryServiceImpl implements CategoryService {

    @Autowired
    private CategoryRepository categoryRepository;

    @Override
    public List<Category> findAll() {
        return categoryRepository.findAll();
    }

    @Override
    public Category findById(Long id) {
        return categoryRepository.findById(id).orElse(null);
    }
}
